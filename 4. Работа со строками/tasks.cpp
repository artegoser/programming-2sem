#include <iostream>
#include <string>

// ============================================================
// Задание 1: Замена ':' на ';', подсчёт замен
// ============================================================

void task1() {
    std::cout << "========================================\n";
    std::cout << "  Задание 1: Замена ':' на ';'\n";
    std::cout << "========================================\n";

    std::string s;
    std::cout << "Введите строку: ";
    std::getline(std::cin, s);

    int count = 0;
    for (char& c : s) {
        if (c == ':') {
            c = ';';
            count++;
        }
    }

    std::cout << "Результат:       " << s << "\n";
    std::cout << "Количество замен: " << count << "\n\n";
}

